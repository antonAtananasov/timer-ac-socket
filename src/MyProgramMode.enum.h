#ifndef MyProgramMode_ENUM
#define MyProgramMode_ENUM

// last three bits are for submodes
enum MyProgramMode : uint8_t
{
    PROGRAM_DISPLAY_TIME = 0b00000000u,

    PROGRAM_SET_TIME = 0b00001000u,
    PROGRAM_SET_TIME_AMPM = 0b00001001,
    PROGRAM_SET_TIME_HOUR = 0b00001010,
    PROGRAM_SET_TIME_MINUTE = 0b00001011,

    PROGRAM_SET_SOCKETS = 0b00010000,
    PROGRAM_SET_SOCKETS_AMPM = 0b00010001,
    PROGRAM_SET_SOCKETS_HOUR = 0b00010010,
    PROGRAM_SET_SOCKETS_MINUTE = 0b00010011,
    PROGRAM_SET_SOCKETS_STATES = 0b00010101,

    PROGRAM_TEST = 0b00100000,

    PROGRAM_UNKNOWN_MODE = 255
};

#endif